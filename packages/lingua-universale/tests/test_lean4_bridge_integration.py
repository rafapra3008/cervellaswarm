class TestTheoremCount:
    @pytest.mark.parametrize("name,protocol", FLAT_PROTOCOLS.items())
    def test_flat_protocol_has_five_theorems(self, name, protocol):
        gen = Lean4Generator()
        code = gen.generate_theorems(protocol)
        count = len(re.findall(r"^theorem ", code, re.MULTILINE))
        assert count == 5, f"{name}: expected 5 theorems, got {count}"

    def test_architect_flow_has_seven_theorems(self):
        gen = Lean4Generator()
        code = gen.generate_theorems(ArchitectFlow)
        count = len(re.findall(r"^theorem ", code, re.MULTILINE))
        assert count == 7, f"ArchitectFlow: expected 7 theorems, got {count}"

    def test_custom_choice_protocol_has_seven_theorems(self):
        """Protocol with one choice gets 5 + 2 = 7 theorems."""
        proto = Protocol(
            name="WithChoice",
            roles=("a", "b", "c"),
            elements=(
                ProtocolChoice(
                    decider="a",
                    branches={
                        "go": (
                            ProtocolStep(sender="a", receiver="b", message_kind=MessageKind.DM),
                        ),
                        "stop": (
                            ProtocolStep(sender="a", receiver="c", message_kind=MessageKind.BROADCAST),
                        ),
                    },
                ),
            ),
        )
        gen = Lean4Generator()
        code = gen.generate_theorems(proto)
        count = len(re.findall(r"^theorem ", code, re.MULTILINE))
        assert count == 7


