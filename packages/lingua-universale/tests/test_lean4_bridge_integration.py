class TestGeneratedStructure:
    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_has_auto_generated_header(self, name, protocol):
        code = generate_lean4(protocol)
        assert "Auto-generated by CervellaSwarm Lean 4 Bridge" in code

    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_has_protocol_name_in_header(self, name, protocol):
        code = generate_lean4(protocol)
        assert f"Protocol: {name}" in code

    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_has_do_not_edit_comment(self, name, protocol):
        code = generate_lean4(protocol)
        assert "DO NOT EDIT" in code

    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_types_section_comes_before_protocol_section(self, name, protocol):
        code = generate_lean4(protocol)
        # "-- Types" only appears once (as section header), after the file header
        types_pos = code.index("-- Types\n")
        # The protocol section is marked by a separator + protocol name line.
        # We find the section separator that immediately precedes "-- Protocol: {name}"
        # and comes AFTER the Types section.
        # The separator line is "-- ============..." followed by "-- Protocol: {name}"
        section_marker = f"-- ============================================================\n-- Protocol: {name}\n-- ======"
        protocol_section_pos = code.index(section_marker)
        assert types_pos < protocol_section_pos

    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_protocol_section_comes_before_theorems_section(self, name, protocol):
        code = generate_lean4(protocol)
        protocol_pos = code.index(f"-- Protocol: {name}")
        theorems_pos = code.index("-- Theorems")
        assert protocol_pos < theorems_pos

    @pytest.mark.parametrize("name,protocol", STANDARD_PROTOCOLS.items())
    def test_message_kind_before_protocol_step(self, name, protocol):
        code = generate_lean4(protocol)
        mk_pos = code.index("inductive MessageKind")
        ps_pos = code.index("structure ProtocolStep")
        assert mk_pos < ps_pos


