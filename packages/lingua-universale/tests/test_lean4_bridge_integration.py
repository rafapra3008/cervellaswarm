def make_custom_protocol(name="Custom", roles=("x", "y"), kind=MessageKind.DM) -> Protocol:
    """Build a simple custom (non-standard) flat protocol."""
    return Protocol(
        name=name,
        roles=roles,
        elements=(
            ProtocolStep(sender=roles[0], receiver=roles[1], message_kind=kind),
        ),
    )


