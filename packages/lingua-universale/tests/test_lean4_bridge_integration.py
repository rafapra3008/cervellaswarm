class TestLean4VerifierMocked:
    def test_verify_code_calls_subprocess_with_lean_json(self):
        """verify_code uses lean --json."""
        with mock.patch(
            "cervellaswarm_lingua_universale.lean4_bridge.lean4_available",
            return_value=True,
        ):
            with mock.patch("subprocess.run") as mock_run:
                mock_run.return_value = mock.Mock(
                    stdout="",
                    stderr="",
                    returncode=0,
                )
                verifier = Lean4Verifier(timeout=5)
                result = verifier.verify_code("-- lean code")
                assert result == []
                mock_run.assert_called_once()
                call_args = mock_run.call_args[0][0]
                assert "lean" in call_args
                assert "--json" in call_args

    def test_verify_code_parses_json_output(self):
        """JSON lines from lean output are parsed into dicts."""
        import json

        msg = {"severity": "info", "data": "loaded"}
        with mock.patch(
            "cervellaswarm_lingua_universale.lean4_bridge.lean4_available",
            return_value=True,
        ):
            with mock.patch("subprocess.run") as mock_run:
                mock_run.return_value = mock.Mock(
                    stdout=json.dumps(msg) + "\n",
                    stderr="",
                    returncode=0,
                )
                verifier = Lean4Verifier()
                result = verifier.verify_code("-- lean code")
                assert result == [msg]

    def test_verify_code_ignores_invalid_json_lines(self):
        """Non-JSON lines (e.g. progress output) are silently skipped."""
        valid_json = '{"severity": "info", "data": "ok"}'
        with mock.patch(
            "cervellaswarm_lingua_universale.lean4_bridge.lean4_available",
            return_value=True,
        ):
            with mock.patch("subprocess.run") as mock_run:
                mock_run.return_value = mock.Mock(
                    stdout="not json\n" + valid_json + "\nalso not json\n",
                    stderr="",
                    returncode=0,
                )
                verifier = Lean4Verifier()
                result = verifier.verify_code("-- code")
                assert len(result) == 1
                assert result[0]["severity"] == "info"

    def test_verify_code_raises_timeout_error(self):
        """subprocess.TimeoutExpired is converted to TimeoutError."""
        import subprocess

        with mock.patch(
            "cervellaswarm_lingua_universale.lean4_bridge.lean4_available",
            return_value=True,
        ):
            with mock.patch("subprocess.run") as mock_run:
                mock_run.side_effect = subprocess.TimeoutExpired(cmd="lean", timeout=5)
                verifier = Lean4Verifier(timeout=5)
                with pytest.raises(TimeoutError, match="timed out"):
                    verifier.verify_code("-- code")

    def test_verify_code_cleans_up_temp_file(self):
        """Temp file is deleted even on success."""
        import tempfile

        created_files = []

        original_ntf = tempfile.NamedTemporaryFile

        def capturing_ntf(*args, **kwargs):
            f = original_ntf(*args, **kwargs)
            created_files.append(f.name)
            return f

        with mock.patch(
            "cervellaswarm_lingua_universale.lean4_bridge.lean4_available",
            return_value=True,
        ):
            with mock.patch("subprocess.run") as mock_run:
                mock_run.return_value = mock.Mock(stdout="", stderr="", returncode=0)
                with mock.patch("tempfile.NamedTemporaryFile", side_effect=capturing_ntf):
                    verifier = Lean4Verifier()
                    verifier.verify_code("-- code")

        # All created temp files should be deleted
        import os

        for path in created_files:
            assert not os.path.exists(path), f"Temp file not cleaned up: {path}"

